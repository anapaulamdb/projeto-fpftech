        <h2>História</h2>

        <p class="texto">Nossa empresa é uma pizzaria nova no mercado. A casa, aberta em 2017, conta com um amplo espaço com capacidade para x pessoas (confortavelmente). Nossa cozinha oferece 9 sabores de pizzas e variadas massas caseiras, seguindo as receitas originais das cidades da Itália.</p>
        <p class="texto">Nossas pizzas são feitas com a melhor farinha existente no mercado. Utilizamos as melhores marcas de queijos e embutidos e nossos vegetais são escolhidos um a um para você poder apreciar uma pizza de qualidade e muito saborosa.</p>
        <p class="texto">A qualidade e o sabor das nossas pizzas, bem como o atendimento da nossa equipe, é, sem dúvidas, o nosso maior orgulho e a razão de uma clientela amiga e cativa que nos acompanha por todos esses anos.</p>

        <h2>Missão</h2>
        <p class="texto">Produzir e comercializar pizzas e massas nos quais os clientes se sintam satisfeitos e felizes ao comer, aliando um bom produto a serviços de atendimento e entrega com alta qualidade, criando sentido na existência e sustentabilidade do negócio.</p>

        <h2>Visão</h2>
        <p class="texto">Estar entre as principais marcas do ramo de pizzaria, em especial delivery, e ser uma marca respeitada e consagrada no quesito qualidade dos produtos e serviços prestados.</p>

